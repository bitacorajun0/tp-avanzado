                    <div class="redondoJ">
                        <h2>2. Características</h2>
                    </div>
                    <p>La mayoría de los cactus, defienden sus reservas de agua a toda costa y lo hacen
                        desarrollando unas espinas. En general, el cuerpo viene conformado por un tallo, una
                        estructura de color verde y engrosada debido al desarrollo de tejidos fundamentales que
                        prevalecen. Hay 3 formas principales de tallo: <br>
                    <ul class="listaJ">
                        <li>Columnar: Tallo en forma cilíndrica (con o sin ramificación).</li>
                        <li>Cladodio: Tallo aplanado en forma de raqueta.</li>
                        <li>Globoso: Tallo casi esférico, con porte de barril.</li>
                    </ul><br>
                    Estas plantas crasas presentan una areola, que es una yema axilar donde crecen las espinas o
                    pelos, una estructura vegetal que se encuentra sobre los podarios (la base foliar dilatada y
                    expandida en forma de pequeño tubérculo) y las costillas.<br>
                    En la zona superior se producen las flores que suelen ser hermafroditas, y la zona inferior
                    los meristemos espinulíferos. <br>
                    Los cactus tienen espinas que salen de una especie de grano, donde crecerá la flor. Las
                    plantas que tienen hojas carnosas y que no tienen espinas o que tienen espinas que no salen
                    de areolas, son llamadas comúnmente suculentas. Las espinas pueden parecer hostiles, pero
                    forman parte de la estrategia de supervivencia de la planta, pues aseguran protección contra
                    la voracidad de los herbívoros.<br>
                    Las raices son muy largas y no van hacia abajo. Las raíces de los cactus se extienden en
                    horizontal cientos de metros. ¿Por qué? Porque así crean una base estable para que se
                    mantengan rectos. Y un dato más sobre el riego: si los mojas continuamente, estas raíces
                    fines se pudren.
                    </p>
