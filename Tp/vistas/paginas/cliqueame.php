                        <h2>3.Cuidados</h2>
                    </div>
                    <p>Si bien las cactáceas son plantas poco exigentes, requieren algunos cuidados. Necesitan de
                        mucho sol para desarrollarse, pero la acción directa de sus rayos en la parte más rigurosa
                        del verano puede dañarlas. Por eso, cuando se cultivan cactus en macetas, conviene no
                        dejarlos durante esa época del año todo el tiempo al sol.<br>
                        Con relación a la humedad, hay que prestar atención para no excederse, pero también para
                        proporcionarle agua suficiente. Estas plantas poseen una enorme capacidad para almacenar
                        líquido en sus raíces, tallo y hojas y, además, su superficie es pilosa, lo que le permite
                        capturar humedad del ambiente. Lo aconsejable es regarlas con una frecuencia de una vez al
                        mes en invierno y una vez a la semana en el periodo veraniego.<br>
                        Y también es importante procurar que estén en una tierra que les pueda aportar los
                        nutrientes necesarios para su desarrollo. Conviene abonar el sustrato en el que se
                        encuentren y, después de unos años, trasplantarlos a un recipiente mayor.<br>
                        Los cactus ofrecen una dificultad añadida con respecto a muchas otras especies vegetales al
                        momento de trasplantarlos: las espinas que ocupan la casi totalidad de su cuerpo. Una
                        técnica para realizar esta operación es envolverlos con papeles para evitar los pinchazos.
                        Una doble o triple capa de papel de periódico impide, en general, que las espinas se claven
                        en las manos y, al mismo tiempo, permiten manejar las plantas con suficiente libertad y sin
                        causarles daño.<br>
                        Además, necesitan tierras que drenen bien el agua, es decir, mejor que sean arenosas y con
                        piedras. Si lo vas a trasplantar, tenlo en cuenta. Y no hagas como con el resto de plantas,
                        o sea, no lo riegues justo después de trasplantarlo. Espera entre una semana y diez
                        días.
                    </p>
