                        <h2>1. ¿Qué son los cactus?</h2>
                    </div>
                    <p>Los cactus son un tipo de plantas que disponen de órganos especiales para almacenar agua y
                        nutrientes en sus tejidos de manera muy superior a la de otras plantas, y conforman la
                        familia de plantas suculentas denominada Cactaceae.<br>
                        El término cactus procede de la lengua latina, aunque su raíz etimológica se halla en el
                        griego káktos, el concepto hace referencia a una planta que pertenece a la familia de las
                        cactáceas.<br>
                        Los cactus constituyen la más famosa familia de plantas suculentas y una de las más
                        populares en
                        general, debido sobre todo a su aparición en innumerables películas y dibujos
                        animados. Originarios del continente americano, los cactus se clasifican en más de 200
                        géneros
                        que incluyen unas 2.500 especies, adaptadas a los climas más áridos.<br>
                        Una de las características que debemos de resaltar, es su poder de aclimatación. Estas
                        plantas
                        han evolucionado para sobrevivir en el desierto. Actualmente han pasado a formar
                        parte de los hogares con un papel ornamental, en todo el mundo y con diferentes climas.
                    </p>
                </div>
